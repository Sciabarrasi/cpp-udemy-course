
#include <iostream>
#include <cmath>

using namespace std;

int main () {

    double power = pow(5,5);
    double root = sqrt(100.0);
    double sine = sin(0.523599);
    double cosine = cos(1.0472);
    double tangent = tan(0.785398);

    cout<<"Power: "<<power<<endl;
    cout<<"Root: "<<root<<endl;
    cout<<"Sine: "<<sine<<endl;
    cout<<"Cosine; "<<cosine<<endl;
    cout<<"Tangent: "<<tangent<<endl;

    return 0;
}
